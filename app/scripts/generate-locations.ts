// Generate slugs
function generateSlug(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');
}

// Generate location data with slugs
export function generateLocationData() {
  const states = statesData.map(state => ({
    ...state,
    slug: generateSlug(state.name),
  }));

  const cities: Array<{
    name: string;
    slug: string;
    stateAbbr: string;
    stateName: string;
    population: number;
    zipCodes: string[];
    latitude: number;
    longitude: number;
  }> = [];
  states.forEach(state => {
    const stateCities = citiesByState[state.abbreviation] || [];
    stateCities.forEach(city => {
      cities.push({
        ...city,
        slug: generateSlug(city.name),
        stateAbbr: state.abbreviation,
        stateName: state.name,
        stateSlug: state.slug,
      });
    });
  });

  return { states, cities };
}

// Save to JSON file
export function saveLocationData() {
  const data = generateLocationData();
  const dataDir = path.join(process.cwd(), 'data');

  if (!fs.existsSync(dataDir)) {
    fs.mkdirSync(dataDir, { recursive: true });
  }

  fs.writeFileSync(
    path.join(dataDir, 'states.json'),
    JSON.stringify(data.states, null, 2)
  );

  fs.writeFileSync(
    path.join(dataDir, 'cities.json'),
    JSON.stringify(data.cities, null, 2)
  );

  console.log(` Generated ${data.states.length} states and ${data.cities.length} cities`);
  console.log(` Saved to data/states.json and data/cities.json`);
}

